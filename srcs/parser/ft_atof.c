double	ft_get_double_value(char const *s)
{
	static char	buf[0x100];
	char		*p;
	size_t		i;
	bool		found;

	p = buf;
	ft_bzero((void *)buf, 0x100);
	while (*s != 0)
	{
		i = 0;
		found = false;
		while (g_pi[i] != 0)
		{
			if (!found && ft_strnequ(s, g_pi[i], ft_strlen(g_pi[i])) != 0)
			{
				found = true;
				ft_strcpy(p, g_pi[i + 1]);
				s += ft_strlen(g_pi[i]);
			}
			i = i + 1;
		}
		if (found == false)
			*p++ = *s++;
	}
	return (atof(buf));
}
