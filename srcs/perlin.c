static float	fade(float t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

static float	lerp(float t, float a, float b)
{
	return (a + t * (b - a));
}

static float	grad(int hash, float x, float y, float z)
{
	int			h;
	float		u;
	float		v;

	h = hash & 15;
	u = h < 8 ? x : y;
	if (h < 4)
		v = y;
	else if (h == 12 || h == 14)
		v = x;
	else
		v = z;
	u = (h & 1) == 0 ? u : -u;
	v = (h & 2) == 0 ? v : -v;
	return (u + v);
}

float			ft_fnoise(float x, float y, float z)
{
	t_perl		perl;

	perl.x = (int)floorf(x) & 255;
	perl.y = (int)floorf(y) & 255;
	perl.z = (int)floorf(z) & 255;
	x -= floorf(x);
	y -= floorf(y);
	z -= floorf(z);
	perl.u = fade(x);
	perl.v = fade(y);
	perl.w = fade(z);
	perl.a = g_p[perl.x] + perl.y;
	perl.aa = g_p[perl.a] + perl.z;
	perl.ab = g_p[perl.a + 1] + perl.z;
	perl.b = g_p[perl.x + 1] + perl.y;
	perl.ba = g_p[perl.b] + perl.z;
	perl.bb = g_p[perl.b + 1] + perl.z;
	return (lerp(perl.w, lerp(perl.v, lerp(perl.u, grad(g_p[perl.aa], x, y, z),
						grad(g_p[perl.ba], x - 1, y, z)),
					lerp(perl.u, grad(g_p[perl.ab], x, y - 1, z),
						grad(g_p[perl.bb], x - 1, y - 1, z))),
				lerp(perl.v, lerp(perl.u, grad(g_p[perl.aa + 1], x, y, z - 1),
						grad(g_p[perl.ba + 1], x - 1, y, z - 1)),
					lerp(perl.u, grad(g_p[perl.ab + 1], x, y - 1, z - 1),
						grad(g_p[perl.bb + 1], x - 1, y - 1, z - 1)))));
}
